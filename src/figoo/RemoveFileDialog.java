    public RemoveFileDialog(FigooView fg, java.awt.Frame parent, boolean modal, String file) {
        super(parent, modal);
        this.setTitle("Move");
        Dimension d = Toolkit.getDefaultToolkit().getScreenSize();
        int w = this.getSize().width;
        int h = this.getSize().height;
        this.f = fg;
        int left = (d.width - w) / 2;
        int top = (d.height - h) / 2;
        this.setLocation(left, top);
        initComponents();

        jProgressBar1.setIndeterminate(true);
        task = new RemoveTask(file,this);

        //task.addPropertyChangeListener(((JButton)evt.getSource()).getPropertyChangeListeners()[0]);
        task.addPropertyChangeListener(
                new PropertyChangeListener() {

                    @Override
                    public void propertyChange(PropertyChangeEvent evt) {
                        if ("progress".equals(evt.getPropertyName())) {
                            getjProgressBar1().setValue((Integer) evt.getNewValue());
                        }
                    }
                });
        task.execute();


    }

